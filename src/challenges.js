function footballPoints(wins, ties) {
  return ((wins * 3) + (ties * 1));
}

// Desafio 6
function highestCount(number) {
  let largestNumber = Math.max.apply(null, number)
  let result = 0;
  for (let index = 0; index < number.length; index += 1) {
    if (number[index] === largestNumber) {
      result += 1;
    }
  } return result;
}

